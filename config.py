def check_drug_id(id_set=None):
    if id_set is None:
        id_set = set()
    with open('DN_info.txt', errors='ignore') as f:
        for line in f:
            line_list = line.strip().split('\t')
            drug_id = line_list[0]
            id_set.add(drug_id)
    with open('Domestic_drug_BN_info.txt', errors='ignore') as f:
        for line in f:
            line_list = line.strip().split('\t')
            drug_id = line_list[0]
            id_set.add(drug_id)
    with open('imported_drug_BN_info.txt', errors='ignore') as f:
        for line in f:
            line_list = line.strip().split('\t')
            drug_id = line_list[0]
            id_set.add(drug_id)
    with open('imported_drug_Name_info.txt', errors='ignore') as f:
        for line in f:
            line_list = line.strip().split('\t')
            drug_id = line_list[0]
            id_set.add(drug_id)
    return id_set


